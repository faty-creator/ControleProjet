    private void bnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnAddActionPerformed
        String nom = txtNom.getText().toString();
        String prenom = txtPrenom.getText().toString();
        java.util.Date utilDate = txtDate.getDate(); 
        java.sql.Date dateNaissance = new java.sql.Date(utilDate.getTime());
        String email = txtEmail.getText().toString();

        if (es.create(new Etudiant(nom, prenom, (java.sql.Date) dateNaissance, email))) {
            JOptionPane.showMessageDialog(this, "Bien enregistré");
            loadEt();
        } else {
            JOptionPane.showConfirmDialog(this, "Problème");
        }


    }//GEN-LAST:event_bnAddActionPerformed

    private void tableEtudiantMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableEtudiantMouseClicked
        id = Integer.parseInt(model.getValueAt(tableEtudiant.getSelectedRow(), 0).toString());
        txtNom.setText(model.getValueAt(tableEtudiant.getSelectedRow(), 1).toString());
        txtPrenom.setText(model.getValueAt(tableEtudiant.getSelectedRow(), 2).toString());
        String dateString = model.getValueAt(tableEtudiant.getSelectedRow(), 3).toString();
        try {
            java.util.Date utilDate = new SimpleDateFormat("yyyy-MM-dd").parse(dateString); // format à ajuster selon ton format de date
            txtDate.setDate(utilDate);
        } catch (ParseException e) {
            e.printStackTrace(); //  l'exception 
        }
        txtEmail.setText(model.getValueAt(tableEtudiant.getSelectedRow(), 4).toString());
    }//GEN-LAST:event_tableEtudiantMouseClicked
