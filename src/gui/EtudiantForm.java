    private void bnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnDeleteActionPerformed
        int selectedRow = tableEtudiant.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner un étudiant à supprimer.");
            return;
        }

        int response = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment supprimer cet étudiant ?", "Confirmation", JOptionPane.YES_NO_OPTION);

        if (response == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
            if (es.delete(es.findById(id))) {
                JOptionPane.showMessageDialog(this, "Étudiant supprimé avec succès.");
                loadEt();
            } else {
                JOptionPane.showMessageDialog(this, "Erreur lors de la suppression de l'étudiant.");
            }
        }    }//GEN-LAST:event_bnDeleteActionPerformed
//update
    private void bnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnUpdateActionPerformed
        int response = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment modifier ?", "Confirmation", JOptionPane.YES_NO_OPTION);
        if (response == JOptionPane.YES_OPTION) {
            String nom = txtNom.getText().trim();
            String prenom = txtPrenom.getText().trim();

            //  convertir en java.sql.Date
            java.util.Date utilDate = txtDate.getDate();
            if (utilDate == null) {
                JOptionPane.showMessageDialog(this, "Veuillez sélectionner une date.");
                return;
            }
            java.sql.Date dateNaissance = new java.sql.Date(utilDate.getTime());

            String email = txtEmail.getText().trim();

            if (es.update(new Etudiant(id, nom, prenom, dateNaissance, email))) {
                JOptionPane.showMessageDialog(this, "Étudiant modifié avec succès !");
                loadEt();
            } else {
                JOptionPane.showMessageDialog(this, "Erreur lors de la modification.", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        }
     }//GEN-LAST:event_bnUpdateActionPerformed


