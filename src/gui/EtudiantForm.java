    public void loadEt() {
        model.setRowCount(0);
        for (Etudiant e : es.findAll()) {
            model.addRow(new Object[]{e.getId(), e.getNom(), e.getPrenom(), e.getDateNaissance(), e.getEmail()});
        }
    }

