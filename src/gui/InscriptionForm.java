    private void loadCours() {
        txtCours.removeAllItems();
        for (Cours cours : coursService.findAll()) {
            txtCours.addItem(cours);
        }
    }

    private void loadEtudiants() {
        txtEtudiant.removeAllItems();
        for (Etudiant etudiant : etudiantService.findAll()) {
            txtEtudiant.addItem(etudiant);
        }
    }

    private void loadInscriptions() {
        tableModel.setRowCount(0);
        for (Inscription inscription : inscriptionService.findAll()) {
            tableModel.addRow(new Object[]{
                inscription.getCours().getIntitule(),
                inscription.getEtudiant().getNom(),
                inscription.getDateInscription()
            });
        }
    }

